    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            DefaultTableModel model=(DefaultTableModel)Table.getModel();
          Class.forName("com.mysql.cj.jdbc.Driver");
          Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/Hospital","root","gauri@mysql");
            PreparedStatement pst=con.prepareStatement("SELECT * FROM patient");
              ResultSet rs=pst.executeQuery();
          while(rs.next()){
              int patientid=rs.getInt("ID");
              String pid=Integer.toString(patientid);
              String pname=rs.getString("Name");
              int page=rs.getInt("Age");
              String pAge=Integer.toString(page);
              String dname=rs.getString("DoctorName");
              String row[]={pid,pname,pAge,dname};
              model.addRow(row);              
          }
          con.close();
           }catch(Exception e){
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

